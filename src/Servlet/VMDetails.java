package Servlet;

import java.util.List;

public class VMDetails {
	private List<String> imasList;
	private List<String> flavorList;
	private List<String> networkList;
	public List<String> getImasList() {
		return imasList;
	}
	public void setImasList(List<String> imasList) {
		this.imasList = imasList;
	}
	public List<String> getFlavorList() {
		return flavorList;
	}
	public void setFlavorList(List<String> flavorList) {
		this.flavorList = flavorList;
	}
	public List<String> getNetworkList() {
		return networkList;
	}
	public void setNetworkList(List<String> networkList) {
		this.networkList = networkList;
	}
	
}
